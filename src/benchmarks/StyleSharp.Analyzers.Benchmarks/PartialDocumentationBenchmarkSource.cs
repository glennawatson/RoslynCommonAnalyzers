namespace StyleSharp.Analyzers.Benchmarks;

/// <summary>Builds synthetic source for partial-documentation analyzer (SST1601) benchmarks.</summary>
internal static class PartialDocumentationBenchmarkSource
{
    /// <summary>Builds a compilation unit that exercises clean or violating partial documentation.</summary>
    /// <param name="types">The number of synthetic types to emit.</param>
    /// <param name="violating">Whether to emit partial-documentation violations.</param>
    /// <returns>The generated source text.</returns>
    public static string Generate(int types, bool violating)
        => $$"""
           namespace Bench;

           {{BenchmarkSourceText.JoinBlocks(types, i => GenerateType(i, violating))}}
           """;

    /// <summary>Builds one clean or violating partial type.</summary>
    /// <param name="index">The synthetic type index.</param>
    /// <param name="violating">Whether to emit a violating type.</param>
    /// <returns>The generated type block.</returns>
    private static string GenerateType(int index, bool violating)
        => violating ? GenerateViolatingType(index) : GenerateCleanType(index);

    /// <summary>Builds one clean, documented exposed partial type.</summary>
    /// <param name="index">The synthetic type index.</param>
    /// <returns>The generated type block.</returns>
    private static string GenerateCleanType(int index)
        => $$"""
           /// <summary>A container.</summary>
           public partial class C{{index}}
           {
           }
           """;

    /// <summary>Builds one violating, undocumented exposed partial type.</summary>
    /// <param name="index">The synthetic type index.</param>
    /// <returns>The generated type block.</returns>
    private static string GenerateViolatingType(int index)
        => $$"""
           public partial class C{{index}}
           {
           }
           """;
}
